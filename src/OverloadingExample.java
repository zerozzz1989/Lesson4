public class OverloadingExample {
    public int plus(int a, int b) {
        return a + b;
    }

    public float plus(float a, float b) {
        return a + b;
    }

    public double plus(double a, double b) {
        return a + b;
    }

    public String plus(String a, String b) {
        return a + b;
    }

}
